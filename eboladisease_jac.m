function J=eboladisease_jac(t,u)
np=size(u,2);
if np==1
    J=zeros(5,5);
    J(1,1)=0.9704-0.8111*u(2)-0.5431*u(3);
    J(1,2)=-0.8111*u(1);
    J(1,3)=-0.5431*u(1);
    J(2,1)=0.8111*u(2);
    J(2,2)=0.8111*u(1)-0.7631*u(3)-0.2938;
    J(2,3)=-0.7631*u(2);
    J(3,1)=0.5431*u(3)-1.4809;
    J(3,2)=0.7631*u(3);
    J(3,3)=0.7631*u(2)+0.5431*u(1);
    J(4,2)=0.05;
    J(4,4)=0.149;
    J(5,2)=0.4389;
    J(5,4)=0.045;
    J(5,5)=-0.6704;
else
    ll=length(t);
    J=zeros(5,5*ll);
    ind=1:ll;
    J(1,ind) = -0.8111*u(1);

    ind=ll+ind;
    J(2,ind) = -10;
    J(5,ind) = 0.4389;

    ind=ll+ind;
    J(4,ind) = 1;

    ind=ll+ind;
    J(4,ind) = -10;
    J(5,ind) = -0.6704;
    ind=ll+ind;
    J(1,ind) = 0;
    J(2,ind) = 0.4389;
    J(3,ind) = 0;
    J(4,ind) = 0.045;
    J(5,ind) = -0.6704; 
end
end